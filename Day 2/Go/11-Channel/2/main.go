package main

import "fmt"

func main() {
	ch := make(chan int, 2)

	ch <- 1
	ch <- 2

	fmt.Println(<-ch)
	fmt.Println(<-ch)
}

/*
1. 缓冲区为空，发送操作阻塞：

当 ch <- 1 执行时，首先检查通道 ch 的缓冲区是否为空。
由于 ch 是新创建的，缓冲区为空，没有空间存储数据。
在 Go 语言中，通道被视为数据通道，发送操作会尝试将数据放入通道。由于没有空间，发送操作会阻塞，直到缓冲区中有空间可用。
阻塞意味着当前发送操作的 goroutine 会暂停执行，直到有空间可用。
2. 缓冲区不为空，发送操作立即完成：

当 ch <- 2 执行时，缓冲区中已经有一个空间（由于之前的 ch <- 1 阻塞并等待）。
因此，发送操作可以立即将数据 2 放入缓冲区，不会阻塞。
发送操作完成后，goroutine 继续执行。
内部结构和调度机制：

通道的内部结构是一个循环队列，由 Go 语言运行时管理。
当发送操作阻塞时，goroutine 被放入一个等待队列，等待通道有空间。
当接收操作发生时，如果通道中有数据，则从队列中移除一个等待的 goroutine 并将其唤醒，该 goroutine 可以从通道中接收数据。
*/
